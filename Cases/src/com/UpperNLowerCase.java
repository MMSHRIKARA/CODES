public class UpperNLowerCase {
	public static void main(String[] args) {
		String str = "Bengaluru is not a best place to lIve";
		System.out.println("I/P : "+str);
		System.out.println("O/P UpperCase : "+str.toUpperCase());
		System.out.println("O/P LowerCase : "+str.toLowerCase());
		
		
	}

}
